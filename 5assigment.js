//assignment operators
let x = 10;
x += 5; // x = x + 5
console.log("After += : " + x); // 15
x -= 3; // x = x - 3
console.log("After -= : " + x); // 12
x *= 2; // x = x * 2
console.log("After *= : " + x); // 24
x /= 4; // x = x / 4
console.log("After /= : " + x); // 6
x %= 4; // x = x % 4
console.log("After %= : " + x); // 2    
x **= 3; // x = x ** 3
console.log("After **= : " + x); // 8
//Combined Example
let a = 5;
a += 10;    // a = a + 10
a *= 2;     // a = a * 2
a -= 5;     // a = a - 5
console.log("Combined Example Result: " + a); // 25 
